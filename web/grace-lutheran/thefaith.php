				<h4 class="text_break">Our Faith</h4>
				<p class="twopush">We believe the three persons of the Trinity are coequal and coeternal, one God. Our Heavenly Father, who loves each one of us and desires to see us in Heaven with Him. Jesus Christ the Son, who became human to suffer and die for the sins of all human beings and rise to life again in the ultimate victory over death and Satan. The Holy Spirit, who creates faith through God’s Word and Sacraments.</p>
				<p class="fourpush">Being “Lutheran”, means we accept and teach the Bible as God’s inspired and inerrant Word. Our teachings can be summarized as:</p>
				<p class="fourpush">Grace alone – God loves the people of the world, even though they are sinful, rebel against Him and do not deserve His love. He sent Jesus, His Son, to love the unlovable and save the ungodly.</p>
				<p class="fourpush">Scripture alone – The Bible is God’s inerrant and infallible Word, in which He reveals His Law and His Gospel of salvation in Jesus Christ. It is the sole rule and norm for Christian doctrine.</p>
				<p class="fourpush">Faith alone – By His suffering and death as the substitute for all people of all time, Jesus purchased and won forgiveness and eternal life for them. Those who hear this Good News and believe it have the eternal life that it offers. God creates faith in Christ and gives people forgiveness through Him.</p>
				<h4 class="text_break">Our Mission</h4>
				<p class="twopush">To see everyone who comes into contact with Grace Lutheran Church and Christian School BE THERE in heaven with us through faith in Jesus Christ. (Matthew 6:19-21; Revelation 7:9-17)</p>
				<h4 class="text_break">Our Vision</h4>
				<p class="twopush">Grace Lutheran Church and Christian School, empowered by the Holy Spirit, exists to impact and influence our culture with the amazing love and truth of Jesus Christ.</p>
				<h4 class="text_break">Our Culture</h4>
				<p class="twopush">We sustain a culture that emphasizes “Building Relationships for Eternity” one person at a time. This means that we commit to creating a caring, loving, supportive, family atmosphere that fosters mutual respect.</p>
				<p class="fourpush">We value each person we contact allowing them to experience our Father’s love through our presence. We recognize and value the diversity of gifts we have received from the Holy Spirit and commit to use these gifts to benefit our united ministry.</p>
				<p class="fourpush">We seek the Holy Spirit’s direction, guidance and power as we focus on reaching out to people with the amazing love and truth of Jesus Christ.</p>
				<p class="fourpush">We build relationships with one another through Bible study, prayer, and worship.</p>
				<h4 class="text_break">Our Core Values</h4>
				<p class="twopush"><strong>Authority</strong> – We believe God’s Word to be our infallible authority. In order to accomplish our mission, we must adhere to the principles and practices revealed in God’s inspired Word (Psalm 119:14-16).</p>
				<p class="fourpush"><strong>Integrity</strong> – In all our dealings, we will follow the highest ethical standards to ensure our stewardship of the resources entrusted to us. We are responsible before God, one another and all people for our programs, relationships, personal lifestyles and expenditures of time and money (Psalm 41:12).</p>
				<p class="fourpush"><strong>Prayer</strong> – Our very existence depends upon the will of God. Prayer demonstrates our dependence on and submission to God and His plan for our lives and ministry (Jeremiah 33:3).</p>
				<p class="fourpush"><strong>Excellence</strong> – We must work to do God’s will, by His power, with all our heart doing everything in a way that brings glory and honor to Him (Proverbs 22:29).</p>
				<p class="fourpush"><strong>Relationship</strong> – We strive to serve and love one another, believing that mutual interdependence, respect and the combining of our gifts are essential to the fulfillment of our mission. (Philippians 2:3).</p>
				<p class="fourpush"><strong>Obedience</strong> – Obedience to the Word and will of God is a prerequisite for the success of any God-honoring life and mission. As a result, we expect obedience to God and view it as a fundamental component to fulfilling the mission of making disciples of Jesus (John 14:15).</p>
				<p class="fourpush"><strong>Creativity</strong> – To create is to make something new from something old. Because the gospel is the best news that anyone could ever receive, we refuse to present it as dull or boring. We accurately translate and publish the Bible’s inherent vibrancy and color with presentations that are attractive, fresh, intriguing, relevant and compelling (Ephesians 3:8-9).</p>
				<p class="fourpush"><strong>Faithfulness</strong> – Faithful in daily devotions and daily prayer; faithful in weekly corporate worship; faithful in pursuing spiritual maturity through Bible Study; faithful in sharing Jesus with our world; faithful in putting our faith into action; faithful in serving Jesus Christ and faithful in consistent, sacrificial giving (Psalm 119).</p>
